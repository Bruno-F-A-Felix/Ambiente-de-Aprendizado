public class mult {
    public static void main(String entrada[]) {
        //Declaração de variaveis
        int n1, n2, soma;

        //Entrada de dados
        n1 = Integer.parseInt(entrada[0]);
        n2 = Integer.parseInt(entrada[1]);

        //Processamento
        soma = n1 + n2;
        
        //Saída dos resultados
        System.out.println(n1 + " + " + n2 + " = " + soma);
        System.exit(0);
    }
}
